     <h2>Crítica.</h2></div>
     <div class="row center-block">
     <p>Ao final de <b>Perdido em Marte</b> (<b>The Martian</b>), fica uma curiosa sensação de improbabilidade. É notável, afinal,
      que o responsável por alguns dos filmes de ficção científica mais influentes e sombrios de todos os tempos, <b>Ridley Scott</b>, seja o diretor de algo tão inusitadamente divertido.</p></div>
     <div class="row center-block">
     <p>Em tempos em que filmes de gênero ficam cada vez mais realistas em tom, o que traz consigo 
     uma carga de sobriedade, Scott vai na contramão da tendência apresentando um sci-fi alegre e repleto de emoções positivas. É quase um longa sobre as origens de um livro de auto-ajuda.</p></div>
    <div class="row center-block">
     <p>Não que isso seja negativo. Depois de errar em Prometheus, 
     em que tentou voltar às suas origens dos anos 70/80, Scott parece ter aceitado seu próprio momento, levando às telas a leveza do romance de <b>Andy Weir</b> adaptado aqui por <b>Drew Goddard</b>.</p></div>
    <div class="row center-block">
     <p>Dessa forma, Perdido em Marte, sobre um astronauta (<b>Matt Damon</b>) tentando sobreviver ao ser deixado para trás pela tripulação de uma missão da NASA em Marte, resulta repleto de otimismo e bom-humor, que inclui várias referências à cultura pop. Essas cenas inteligentes, com diálogos que não se preocupam em explicar demais ciência para leigos,
      predominam entre as econômicas cenas de ação e suspense, que contam com a colorida e sem invencionices fotografia de <b>Dariusz Wolski</b> e um 3D competente.</p></div>
    <div class="row center-block">
     <p>No papel central, Matt Damon desvia-se com competência de qualquer comparação com outros atores que fizeram filmes sobre solidão e sobrevivência e despeja seu carisma através do sistema solar. Fica fácil acreditar que o mundo deseja salvá-lo, 
     assim como já acreditamos no passado que ele merecia ser resgatado na pele do Soldado Ryan. Se tem um sujeito que não pode ficar pra trás, esse cara é Matt Damon.</p></div>
    
      