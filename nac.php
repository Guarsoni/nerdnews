     <h1 >Nova música da Maisa viraliza na internet.</h1></div>
     <div class="row center-block">
	 <p>O caso mais recente de musica chiclete é “Nhe nhe nhem” da pequena cantora e apresentadora do SBT, Maisa. O vídeo da música no YouTube já ultrapassa a marca dos 6
	  milhões de views que tendem a aumentar. Criticada por 
	  boa parte, a canção é sim um “chiclete” afinal ela sim tem idade para cantar este tipo 
	  de música e é isso que seu público precisa. Apesar
	   dos apesares a letra passa uma mensagem que deve ser tratada com atenção, a reclamação que 
	   eu, você e todos nós fazemos no dia a dia, este e o real “Nhe nhe nhem” de que Maisa tanto reclama na música.</p></div>
