	 <div class="row center-block">
	 <p>Em sua letra, Maísa Silva de 13 anos utiliza-se da figura de linguagem onomatopeia para apontar situações do dia a dia em que as pessoas acabam se irritando como “alguém buzinando no ouvido, nhenhem”, porém superam com calma em “respira e conta até cem”.</p></div>
	 <div class="row center-block">
	 <p>No Twitter, ganhou destaque e foi muito comentada por justamente ser uma canção envolvente, alegre e nada comum.</p></div>
	 <div class="row center-block">
	 <p>Os fãs não perderam tempo e já começaram a fazer memes com a canção do gênero pop-chiclete que não se ouvia mais desde “Friday” e “Call Me Maybe”, de Rebecca Black e Carly Rae Jepsen, respectivamente.</p></div>
	 <div class="row center-block">
	 <p>A música atingiu não somente o público infantil, mas também os jovens que acabaram fazendo trocadilhos com o seu título. Vale ressaltar que a mensagem da música é enfrentar com paciência e animação o stress diário.</p></div>
	 <div class="row center-block">
	 <p>NheNheNhem gera um debate em torno da falsa erudição da cultura em que “peritos musicais” julgam gêneros como funk, sertanejo, axé, pagode e, no caso, pop-infantil como inferiores simplesmente por divergirem de suas preferências.</p></div>
	 <div class="row center-block">
	 <p>Sendo amada ou odiada, NheNheNhem está fazendo sucesso, mais um entre os trabalhos de Maisa, que começou desde cedo na televisão e espera-se que continue trazendo alegria e animação para todos por muito mais tempo ainda.</p></div>
	 