     <h1 >'Star Wars': Disney da a fã com câncer terminal chance de ver novo filme</h1></div>
     <div class="row center-block">
     <h2>Após campanha na web, empresa exibiu filme na casa de Daniel Fleetwood.
Ele tem poucas semanas de vida após câncer se espalhar para pulmões.</h2></div>
     <p>
      <img src="manolo.jpg" class="img-responsive center-block" alt="Responsive image">
      </p>     
     <div class="row center-block">
     <p>Um fã de "Star Wars" com câncer terminal assistiu, com ajuda da Disney, a uma versão prévia do novo filme da saga. Ele foi autorizado a assistir o longa após uma campanha nas redes sociais.</p></div>
      <div class="row center-block">
      <p>Outros fãs e até atores como Mark Hamill, John Boyega e Peter Mayhew mostraram seu apoio à campanha para que o americano Daniel Fleetwood, de 32 anos, um doente terminal de câncer, assistisse a "Star Wars: Episódio VII – 
      O despertar da força", novo filme da saga criada por George Lucas, antes que ele chegue aos cinemas. A data de estreia nos Estados Unidos é 18 de dezembro.</p>
       </div>
       <div class="row center-block">
      <p>A ação lançada nas redes sociais fazia o pedido para os estúdios Walt Disney e para o diretor do filme, J.J. Abramsl. 
      Fleetwood se descreve como um grande fã da franquia. No Twitter, Hamill, Boyega e Mayhew mostraram seu apoio à campanha com a hashtag #ForceForDaniel, ou #ForçaParaDaniel.</p>
       </div>
       <div class="row center-block">
      <p>Fletwood, que mora no Texas, sofre um tipo de câncer que se espalhou para os pulmões. Os médicos diagnosticaram em setembro que ele têm apenas mais um ou dois meses de vida.</p>
       </div>
       <div class="row center-block">
      <p>"Gosto muito de 'Star Wars'. A julgar como a doença progrediu nos últimos meses, não acredito que consiga vê-lo no cinema", disse o doente em entrevista à KPRC, emissora local da rede NBC.</p>
       </div>
       <div class="row center-block">
      <p>Fleetwood está sob cuidados em casa ao lado de sua esposa, Ashley, que tornou públicas as intenções do marido através de sua página no Facebook.</p>
       </div>
       
      <div class="row center-block">
      <p>Nesta quinta-feira, Ashley disse em seu perfil no Facebook que a Disley e a Lucasfilms aderiram à campanha. "Eles fizeram seu sonho final se realizar", escreveu. "Agradecemos às pessoas incríveis que ajudaram a isso acontecer, não tenho nem palavras para agradecer. Que a força esteja com todos vocês!."</p>
      </div>